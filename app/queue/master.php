namespace app\queue;

use ext\queue;
use ext\redis;

class master extends queue
{
    public $tz = ['start', 'unit'];

    /**
     * master constructor.
     */
    public function __construct()
    {
        $redis = redis::new()->connect();
        parent::__construct($redis);
    }

    /**
     * 最多启动4个子进程
     *
     * @param int $max_fork
     * @param int $max_exec
     *
     * @throws \Exception
     */
    public function start(int $max_fork = 12, int $max_exec = 1000): void
    {
        $this->go($max_fork, $max_exec); // TODO: Change the autogenerated stub
    }
}
